/**
 * Write a Java program that swaps two variables
 *
 * @author Kristel Talimaa
 *
 */
public class Homework2 {
    public static void main(String[] args) {

        String Audi = "Black";
        switch (Audi) {
            case "White":
                System.out.println("I have a white Audi!");
                break;
            case "Blue":
                System.out.println("I have a blue Audi!");
                break;
            case "Black":
                System.out.println("I have a black Audi!");
                break;
        }
    }
}


